
public class CodigoHuffman {
Comparable letra; 
Comparable cantidad; 
	
public CodigoHuffman (Comparable letra, Comparable cantidad) {
this.letra = letra; this.cantidad = cantidad; 	
}

public Comparable getLetra() {
return this.letra; 	
}

public Comparable getCantidad() {
return this.cantidad; 	
}


}
